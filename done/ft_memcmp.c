int	ft_memcmp(const void *str1, const void *str2, int n)
{
	int			i;
	const char	*s1;
	const char	*s2;

	i = 0;
	s1 = str1;
	s2 = str2;
	while (i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
