unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned	int	lenght;
	unsigned	int	i;

	lenght = 0;
	while (src[lenght] != '\0')
		lenght++;
	i = 0;
	while (src[i] != '\0' && i < size)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (lenght);
}
